package com.company;

public class Enfermero {

    int id;
    String nombre;
    String apellido;
    int estudios;
    int experiencia;

    Enfermero(int id, String name, String ape, int est, int exp){

        this.id = id;
        this.nombre = name;
        this.apellido = ape;
        this.estudios = est;
        this.experiencia = exp;
    }

}
